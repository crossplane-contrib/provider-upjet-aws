package controller

import (
	ctrl "sigs.k8s.io/controller-runtime"

	"github.com/crossplane/upjet/pkg/controller"

	backup "github.com/crossplane-contrib/provider-upjet-aws/internal/controller/fsx/backup"
	datarepositoryassociation "github.com/crossplane-contrib/provider-upjet-aws/internal/controller/fsx/datarepositoryassociation"
	lustrefilesystem "github.com/crossplane-contrib/provider-upjet-aws/internal/controller/fsx/lustrefilesystem"
	ontapfilesystem "github.com/crossplane-contrib/provider-upjet-aws/internal/controller/fsx/ontapfilesystem"
	ontapstoragevirtualmachine "github.com/crossplane-contrib/provider-upjet-aws/internal/controller/fsx/ontapstoragevirtualmachine"
	windowsfilesystem "github.com/crossplane-contrib/provider-upjet-aws/internal/controller/fsx/windowsfilesystem"
)

// Setup_fsx creates all controllers with the supplied logger and adds them to
// the supplied manager.
func Setup_fsx(mgr ctrl.Manager, o controller.Options) error {
	for _, setup := range []func(ctrl.Manager, controller.Options) error{
		backup.Setup,
		datarepositoryassociation.Setup,
		lustrefilesystem.Setup,
		ontapfilesystem.Setup,
		ontapstoragevirtualmachine.Setup,
		windowsfilesystem.Setup,
	} {
		if err := setup(mgr, o); err != nil {
			return err
		}
	}
	return nil
}
